  final String name;
  final String nativeName;
  final String flag;
  final bool isRTL;

  SupportedLanguage({
    required this.code,
    required this.name,
    required this.nativeName,
    required this.flag,
    required this.isRTL,
  });

  Map<String, dynamic> toJson() => {
    'code': code,
    'name': name,
    'nativeName': nativeName,
    'flag': flag,
    'isRTL': isRTL,
  };

  factory SupportedLanguage.fromJson(Map<String, dynamic> json) => SupportedLanguage(
    code: json['code'],
    name: json['name'],
    nativeName: json['nativeName'],
    flag: json['flag'],
    isRTL: json['isRTL'],
  );
}

class TTSVoiceSettings {
  final String languageCode;
  final String voiceId;
  final double speechRate;
  final double pitch;

  TTSVoiceSettings({
    required this.languageCode,
    required this.voiceId,
    required this.speechRate,
    required this.pitch,
  });

  Map<String, dynamic> toJson() => {
    'languageCode': languageCode,
    'voiceId': voiceId,
    'speechRate': speechRate,
    'pitch': pitch,
  };

  factory TTSVoiceSettings.fromJson(Map<String, dynamic> json) => TTSVoiceSettings(
    languageCode: json['languageCode'],
    voiceId: json['voiceId'],
    speechRate: json['speechRate']?.toDouble() ?? 0.5,
    pitch: json['pitch']?.toDouble() ?? 1.0,
  );

  TTSVoiceSettings copyWith({
    String? languageCode,
    String? voiceId,
    double? speechRate,
    double? pitch,
  }) => TTSVoiceSettings(
    languageCode: languageCode ?? this.languageCode,
    voiceId: voiceId ?? this.voiceId,
    speechRate: speechRate ?? this.speechRate,
    pitch: pitch ?? this.pitch,
  );
}

